def _roadmap_to_markdown(roadmap) -> str:
    lines = []
    lines.append("# AI Adoption Roadmap\n")
    lines.append("## Executive Summary\n")
    lines.append(roadmap.executive_summary + "\n")
    lines.append("## Strategic Assessment\n")
    lines.append(roadmap.strategic_assessment + "\n")
    lines.append(f"**AI Readiness Score:** {roadmap.readiness_score}/100\n")

    lines.append("## Use Case Portfolio\n")
    for i, uc in enumerate(roadmap.use_cases, 1):
        lines.append(f"### {i}. {uc.title}")
        lines.append(f"*Priority Score: {uc.priority_score} | Feasibility: {uc.feasibility_score}/10 | ROI: {uc.roi_score}/10 | Risk: {uc.risk_score}/10*\n")
        lines.append(uc.description + "\n")
        lines.append(f"**Business Value:** {uc.business_value}\n")
        lines.append(f"**Approach:** {', '.join(uc.ai_approach)}")
        lines.append(f"**Complexity:** {uc.estimated_complexity} | **Timeline:** {uc.estimated_timeline} | **Cost:** {uc.estimated_cost_tier}\n")
        lines.append(f"**Data Requirements:** {uc.data_requirements}\n")
        lines.append(f"**Team Requirements:** {uc.team_requirements}\n")
        if uc.key_risks:
            lines.append("**Key Risks:**")
            for r in uc.key_risks:
                lines.append(f"- {r}")
            lines.append("")
        if uc.mitigation:
            lines.append("**Mitigation:**")
            for m in uc.mitigation:
                lines.append(f"- {m}")
            lines.append("")

    lines.append(f"## Recommended First Project\n\n{roadmap.recommended_first_project}\n")

    lines.append("## 90-Day Implementation Roadmap\n")
    for milestone in roadmap.roadmap_90_day:
        lines.append(f"### {milestone.week} — {milestone.phase}")
        for d in milestone.deliverables:
            lines.append(f"- {d}")
        lines.append(f"\n**Success Criteria:** {milestone.success_criteria}\n")

    lines.append("## Quick Wins (< 2 weeks)\n")
    for qw in roadmap.quick_wins:
        lines.append(f"- {qw}")
    lines.append("")

    lines.append("## Things to Avoid\n")
    for ta in roadmap.things_to_avoid:
        lines.append(f"- {ta}")
    lines.append("")

    lines.append("## Questions to Answer First\n")
    for q in roadmap.questions_to_answer_first:
        lines.append(f"- {q}")
    lines.append("")

    lines.append("## Readiness Assessment\n")
    lines.append(f"**Score:** {roadmap.readiness_score}/100\n")
    if roadmap.readiness_blockers:
        lines.append("**Blockers:**")
        for b in roadmap.readiness_blockers:
            lines.append(f"- {b}")
        lines.append("")
    if roadmap.readiness_accelerators:
        lines.append("**Accelerators:**")
        for a in roadmap.readiness_accelerators:
            lines.append(f"- {a}")
        lines.append("")

    return "\n".join(lines)


