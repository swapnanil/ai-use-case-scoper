def _render_output(roadmap, fmt: str, output: Optional[Path], verbose: bool) -> None:
    if fmt == "json":
        content = roadmap.model_dump_json(indent=2)
        if output:
            output.write_text(content)
            console.print(f"[green]JSON saved to {output}[/green]")
        else:
            console.print_json(content)

    elif fmt == "markdown":
        content = _roadmap_to_markdown(roadmap)
        if output:
            output.write_text(content)
            console.print(f"[green]Markdown saved to {output}[/green]")
        else:
            console.print(Markdown(content))

    elif fmt == "html":
        content = _roadmap_to_html(roadmap)
        if output:
            output.write_text(content)
            console.print(f"[green]HTML report saved to {output}[/green]")
        else:
            console.print(content)

    else:
        # Default rich terminal output
        _render_rich(roadmap)


